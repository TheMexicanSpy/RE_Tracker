            <p>"Resident Evil 2" es un videojuego de survival horror desarrollado por Capcom. La trama sigue a dos personajes principales, Leon S. Kennedy, un oficial novato de policía, y Claire Redfield, quien busca a su hermano desaparecido, Chris Redfield, un personaje de "Resident Evil". Ambientado en la ficticia ciudad de Raccoon City, el juego se desarrolla durante un brote de un virus mortal conocido como el Virus-T, creado por la corporación Umbrella Corporation.</p>
